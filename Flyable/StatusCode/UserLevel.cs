namespace Flyable.StatusCode;

public enum UserLevel
{
    /// <summary>
    /// 羽翼初展
    /// 代表初始等级，用户像刚出生的小鸟一样，正在展开他们的翅膀准备飞翔。
    /// </summary>
    FeatherStart = 1,
    /// <summary>
    /// 彩石微光
    /// 说明：对于开始了解平台并逐渐活跃起来的用户，就像是小小的彩色宝石开始散发光芒。
    /// </summary>
    ColorStoneGlimmer,
    /// <summary>
    /// 轻羽飞扬
    /// 说明：用户已经对平台有一定了解，并积极参与各种活动，就像羽毛轻轻飘扬在空中。
    /// </summary>
    FeatherFlutter,
    /// <summary>
    /// 彩石闪耀
    /// 说明：开始展现自己的能力和影响力，成为社区内的积极分子，就像彩色宝石开始在阳光下闪耀。
    /// </summary>
    ColorStoneSparkle,
    /// <summary>
    /// 羽翼翩跹
    /// 说明：用户不仅活跃，还经常分享有价值的内容或见解，就像羽毛在空中翩翩起舞。
    /// </summary>
    FeatherDance,
    /// <summary>
    /// 彩石璀璨
    /// 说明：成为社区的重要成员之一，经常帮助解决问题和指导新人，就像彩色宝石在夜空中璀璨夺目。
    /// </summary>
    ColorStoneGlisten,
    /// <summary>
    /// 羽翼翱翔
    /// 说明：在社区内拥有较高声望，是大家公认的专家，就像羽毛在天空中自由翱翔。
    /// </summary>
    FeatherSoar,
    /// <summary>
    /// 彩石辉煌 说明：已经成为社区的标志性人物，对社区发展有着重要贡献，就像彩色宝石在宝箱中发出耀眼的光芒。
    /// </summary>
    ColorStoneRadiance,
    /// <summary>
    /// 羽翼凌空
    /// 说明：社区中的超级明星，几乎无人不知无人不晓，就像羽毛在云端之上自由自在地飞翔。
    /// </summary>
    FeatherAscend,
    /// <summary>
    /// 彩石传说
    /// 说明：最高等级，代表着用户对社区的贡献达到了顶峰，是真正的社区领袖，就像最珍贵的彩色宝石，流传千古。
    /// </summary>
    ColorStoneLegend
}